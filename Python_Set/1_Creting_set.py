# Set is a collection of unique elements, created using curly braces {}
# or the set() function. Sets are unordered so that their elements
# do not have a specific order
#Allowed data types: int, float, str, tuple, complex numbers.
#Disallowed data types: list, dict, set, because these are mutable

my_set = {1, 3.14, "Hello", (5, 6)}
print(f"created set: {my_set}")