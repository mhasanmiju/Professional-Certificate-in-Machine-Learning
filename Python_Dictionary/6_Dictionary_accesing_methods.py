# To retrieve all keys, values, 
# or key-value pairs using the keys(), values(), and items() methods, respectively.

my_dict = {"name": "Miju", "age": 26, "city": "Rangpur"}

key = my_dict.keys()
value = my_dict.values()
item = my_dict.items()

print(f"Key: {key} \nValue: {value} and \nitems: {item}")